    @Override
    public int retornaIdSelecionado() {
       int linha = -1;
       linha = tblBusca.getSelectedRow();
       int id = -1;
       if(linha > -1)
            id = Integer.parseInt(tblBusca.getModel().getValueAt(linha,0).toString());
       
       return id;
    }
    
    @Override
    public void limpaFiltro(){
        filtro.setNome(null);
        filtro.setId(0);
        filtro.setCpf_cnpj(null);
        filtro.setEndereco(null);
        filtro.setTelefone(null);
    }
    
    @Override
    public void preencheFiltro() {
        try{
            if(!txtNome.getText().isEmpty())
                filtro.setNomee(txtNome.getText());
            
            filtro.setTipo((TipoCliente)cbxTipo.getSelectedItem());
            
            if(!txtEndereco.getText().isEmpty())   
                filtro.setEndereco(txtEndereco.getText());
            
            if(!txtTelefone.getText().isEmpty())
                filtro.setTelefone(txtTelefone.getText());
            
            if(!txtcpf_cnpj.getText().isEmpty())   
                filtro.setCpf_cnpj(txtcpf_cnpj.getText());
            
        }catch(ViolacaoRegrasNegocioException ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    @Override
    public void preencheTabela(List<Cliente> listagem) {
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Nome");    
        modelo.addColumn("Tipo");
        modelo.addColumn("CPF/CNPJ");
        modelo.addColumn("Endereço");
        modelo.addColumn("Telefone");
        
        
        for(Cliente c : listagem){
            Vector linha = new Vector();
            linha.add(c.getId());
            linha.add(c.getNome());
            linha.add(c.getTipo().getDescricao());
            linha.add(c.getCpf_cnpj());
            linha.add(c.getEndereco());
            linha.add(c.getTelefone());
            
            modelo.addRow(linha);
            
        }
        
        tblBusca.setModel(modelo);
    }
