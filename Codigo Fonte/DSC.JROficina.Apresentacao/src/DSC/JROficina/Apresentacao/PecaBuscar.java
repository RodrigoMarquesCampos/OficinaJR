    @Override
    public int retornaIdSelecionado() {
       int linha = -1;
       linha = tblBusca.getSelectedRow();
       int id = -1;
       if(linha > -1)
            id = Integer.parseInt(tblBusca.getModel().getValueAt(linha,0).toString());
       
       return id;
    }

    @Override
    public void preencheFiltro() {
        Fornecedor f = new Fornecedor();
        
        if(!txtNome.getText().isEmpty())
            filtro.setNome(txtNome.getText());
        if(!txtCompra.getText().isEmpty())
            filtro.setValor_compra(Float.parseFloat(txtCompra.getText()));
        if(!txtMarca.getText().isEmpty())
            filtro.setMarca(txtMarca.getText());
        if(!txtDesc.getText().isEmpty())
            filtro.setDesc(txtDesc.getText());
        if(!txtVenda.getText().isEmpty())
            filtro.setValor_venda(Float.parseFloat(txtVenda.getText()));
        if(cbxFornecedor.getSelectedItem() != null){
           filtro.setFornecedor((Fornecedor) cbxFornecedor.getSelectedItem());
        }             
    }

    @Override
    public void preencheTabela(List<Peca> listagem) {
         DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Nome");    
        modelo.addColumn("Descri√ß√£o");
        modelo.addColumn("Quantidade");
        modelo.addColumn("Valor Compra");
        modelo.addColumn("Valor Venda");
        modelo.addColumn("Fornecedor");
        
         for(Peca c : listagem){
            Vector linha = new Vector();
            linha.add(c.getId());
            linha.add(c.getNome());
            linha.add(c.getDesc());
            linha.add(c.getQtde());
            linha.add(c.getValor_compra());
            linha.add(c.getValor_venda());
            linha.add(c.getFornecedor().toString());
            
            modelo.addRow(linha);
            
        }    
        tblBusca.setModel(modelo);
    }

    @Override
    public void limpaFiltro() {
        filtro.setNome(null);
        filtro.setId(0);
        filtro.setDesc(null);
        filtro.setQtde(-1);
        filtro.setValor_compra(-1);
        filtro.setValor_venda(-1);
        filtro.setFornecedor(null);
        filtro.setMarca(null);
    }
