    @Override
    public void carregaCampos() {
        txtDesc1.setText(entidade.getDescricao());
        txtValor1.setText(String.valueOf(entidade.getValor()));
    }

    @Override
    public void carregaObjeto() throws ViolacaoRegrasNegocioException {
        entidade.setDescricao(txtDesc1.getText());
        entidade.setValor(Float.parseFloat(txtValor1.getText()));
    }

    @Override
    public boolean verificarCamposObrigatorios() {
        return !txtDesc1.getText().isEmpty() || !txtValor1.getText().isEmpty();
    }
